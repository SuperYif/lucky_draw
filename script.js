document.getElementById('startButton').addEventListener('click', function() {
    document.getElementById('intro').style.display = 'none';
    document.getElementById('draw').style.display = 'block';
});

document.getElementById('drawButton').addEventListener('click', function() {
    // 显示竹签并开始摇动动画
    const stick = document.getElementById('stick');
    stick.style.display = 'block';

    // 停止摇动动画并开始掉落动画
    setTimeout(function() {
        stick.style.animation = 'drop 1s ease-out forwards';
        
        // 模拟抽签结果
        setTimeout(function() {
            const resultNumber = Math.floor(Math.random() * 100); // 0到99的随机数
            const resultText = `签号：${resultNumber + 1}`;
            const resultExplanation = explanations[resultNumber];

            document.getElementById('resultText').innerText = resultText;
            document.getElementById('resultExplanation').innerText = resultExplanation;
            document.getElementById('result').style.display = 'block';

            // 重置竹签的位置和动画
            stick.style.display = 'none';
            stick.style.animation = 'none';
            setTimeout(function() {
                stick.style.animation = '';
            }, 10);
        }, 1000);
    }, 2000);
});
